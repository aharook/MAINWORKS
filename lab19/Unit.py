class AnalizText():
    def rozdilZnak():
        text = input("Введіть текст: ").lower()
        while not text:
            print(" Текст не може бути порожнім. Спробуйте ще раз.")
            text = input("Введіть текст: ").lower()
        count_p = text.count('п')
        count_r = text.count('р')
        
        print(f"Кількість літер 'п': {count_p}")
        print(f"Кількість літер 'р': {count_r}")
        if count_p > count_r:
            print("Літера 'п' зустрічається частіше.")
        elif count_r > count_p:
            print("Літера 'р' зустрічається частіше.")
        else:
            print("Літери 'п' та 'р' зустрічаються однакову кількість разів.")
        print("програма виконанана успішно")
        print(" ")
    
