    def golosni():
        vowels = "аеєиіїоуюя"  
        text = input("Введіть текст: ").lower().split()

        while not text:  
            print("Текст не може бути порожнім. Спробуйте ще раз.")
            text = input("Введіть текст: ").lower().split()

        count = 0
        for word in text:
            if word[0] in vowels:  
                count += 1

        print(f"Кількість слів, що починаються на голосну: {count}")
        print("Програма виконана успішно\n")
        print(" ")
        
